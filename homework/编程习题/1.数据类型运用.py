# 数据类型使用练习
# 问题：定义好自身的属性如：姓名，身高，年龄，爱好等，并且使用变量名输出数据

# 方法一：定义字典直接打印输出
# "name": "顾志强"(键值对)(键不能重复)
data = {"name": "顾志强", "age": 23,
        "height": "1.73", "gender": '男', "hobby": "打篮球"}
print(data["name"])
print(data["age"])
print(data["height"])
print(data["gender"])
print(data["hobby"])

