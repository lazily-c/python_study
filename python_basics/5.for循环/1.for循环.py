
name = "天下第一"

# 遍历 (访问一个字符串的每一个字符)

# 希望这个字符串的字符，一个一个的被打印出来

# 怎么知道循环多少次？
# name有多少个字符，就循环多少次

# 第一次 i = "天"
# 第二次 i = "下"
# 第三次 i = "第"
# 第四次 i = "一"

for i in name:
    print(i)

# for循环后最常用的序列
# 字符串  "1234565"
# 列表   [1, 2, 3, 4, 5]
# 元组   (1, 2, 3, 4, 5)

li = ["元芳", "李白", "李信"]

for i in li:
    print(i)

# for循环有两种方式
# 方式一：遍历序列(字符串、列表、元组)
# 方式二：range()方法




