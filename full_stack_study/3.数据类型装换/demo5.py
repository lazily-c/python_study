# 转义字符("")和('')的用法

print("全名制作人，大家好，我是：\"张三\",我的电话好码是：10086,我的爱好是：篮球")
print('全名制作人，大家好，我是：\'张三\',我的电话好码是：10086,我的爱好是：篮球')

# \n用法(换行)
print('全名制作人，大家好，我是：张三,\n我的电话好码是：10086,\n我的爱好是：篮球')
# \t用法(tab键用法)
print('全名制作人，大家好，我是：张三,\t\t我的电话好码是：10086,\t我的爱好是：篮球')

# \用法(续行符)
print('全名制作人，大家好，我是：张三,\
我的电话好码是：10086,我的爱好是：篮球')


