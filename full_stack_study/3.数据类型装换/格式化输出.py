# 格式化输出：是按一定的格式进行输出数据
# 占位符


# 整数输出
num1 = 10086
num2 = 180.25

# print("我的电话号码是：", num)


# %o 输出八进制
print("输出我的八进制电话号码是：%o" % num1)

# %d 输出十进制
print("输出我的十进制电话号码是：%d" % num1)
print("输出我的十进制电话号码是：%d" % num2)

# %x 输出十六进制
print("输出我的十六进制电话号码是：%x" % num1)


# 输出浮点数
num2 = 180.25
number = 180


# %f
print("我的身高是：%f" % num2)
print("我的身高是：%f" % number)


