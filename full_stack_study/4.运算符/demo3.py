# -*- coding:utf-8 -*-
# 复合赋值运算符
num1 = 10
num2 = 20
num3 = 30
num4 = 40
num5 = 50
num6 = 60
num7 = 70
# (加等、减等、乘等、除等)
num1 += 20   # 等价于num1 = num1 + 20
print(num1)

num2 -= 5    # 等价于num2 = num2 - 5
print(num2)

num3 *= 5    # 等价于num3 = num3 * 5
print(num3)

num4 /= 5    # 等价于num4 = num4 / 5
print(num4)


# (取整等于、取余等于、乘方等于)
num5 //= 7    # 等价于num = num5 // 7
print(num5)

num6 %= 6     # 等价于num = num6 % 6
print(num6)

num7 **= 2    # 等价于num = num7 ** 2
print(num7)
